def sqrt(number):
    """
    Calculate the floored square root of a number

    Args:
       number(int): Number to find the floored squared root
    Returns:
       int: Floored Square Root
    """
    if number == 1:
        return 1

    sqrt = 0
    for i in range(1, number//2):
        if i*i > number:
            return sqrt
        else:
            sqrt = i

    return sqrt


